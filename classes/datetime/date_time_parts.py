# PARTS OF DATETIME OBJECT
from datetime import date, time, datetime, timedelta

# create datetime object
example_date_time = datetime(2024, 10, 15, 17, 30, 25, 100)

# parts of the example_date_time
print("Year", example_date_time.year)
print("Month:", example_date_time.month)
print("Day:", example_date_time.day)
print("Hours:", example_date_time.hour)
print("Minutes:", example_date_time.minute)
print("Seconds:", example_date_time.second)
print("Microseconds:", example_date_time.microsecond)





