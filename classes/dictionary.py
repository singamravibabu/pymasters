# A collection of items is dictionary
# A dictionary is an unordered collection of items

# In dictionary, each item consists of a key/value pair.
# Each value in the dictionary can be referred using key.
# The key value must be unique in a dictionary.

# Syntax for dictionary:
# {key1:value1, key2:value2, key3:value3, key4:value4. ...}
# The value must be immutable and should be unique in a dictionary.